    public static int countBinarySubstrings(String s)           // 8ms
    {
        int zeroes = 0;
        int ones = 0;
        int count = 0;
        boolean flag1 = false , flag2 = false;
        for(int i = 0 ; i < s.length() ; i++)
        {
            char ch = s.charAt(i);
            if(ch == '0')
            {
                if(flag1)
                {
                    zeroes = 0; 
                    flag1 = false;
                }
                zeroes++;
                if(zeroes <= ones)
                {
                    count++;
                }
                flag2 = true;
            }
            else 
            {
                if(flag2)
                {
                    ones = 0;
                    flag2 = false;
                }
                ones++;
                if(ones <= zeroes)
                {
                    count++;
                }
                flag1 = true;
            }
        }
        return count;
    }

    public static int countBinarySubstrings_2(String s)     // 13ms
    {
        int[] group = new int[s.length()];
        int t = 0;
        group[t] = 1;
        
        for(int i = 1 ; i < s.length() ; i++)
        {
            if(s.charAt(i-1) != s.charAt(i))
            {
                group[++t] = 1;
            }
            else
            {
                group[t]++;
            }
        }
        
        int ans = 0;
        for(int i = 1; i <= t ; i++)
        {
            ans += Math.min(group[i-1] , group[i]);
        }
        return ans;
    }

    public static int countBinarySubstrings_3(String s)   
    {
        int ans = 0;
        int prev = 0;
        int curr = 1;
        for(int i = 1 ; i < s.length() ; i++)
        {
            if(s.charAt(i-1) != s.charAt(i))
            {
                ans += Math.min(prev , curr);
                prev = curr;
                curr = 1;
            }
            else
            {
                curr++;
            }
        }
        
        return ans + Math.min(prev , curr);
    }




