    public static String toLowerCase(String str) 
    {
        StringBuilder sb = new StringBuilder(str);
        for(int i = 0 ; i < sb.length() ; i++)
        {
            char ch = sb.charAt(i);
            if(ch >= 'A' && ch <= 'Z')
            {
                char c = (char)(ch - 'A' + 'a');
                sb.setCharAt(i , c);
            }
        }
        
        return sb.toString();
    }



