    {
        int[] results = new int[queries.length];
        int[] freq = new int[11];       // as length of string in words and queries is < 11
        
        for(int i = 0 ; i < words.length ; i++)
        {
            int runningFreq = calculateFrequency(words[i]);
            freq[runningFreq - 1]++;
        }
        for(int i = 9 ; i >= 0 ; i--)
        {
            freq[i] += freq[i + 1];
        }
        
        for(int i = 0; i < queries.length ; i++)
        {
            int f = calculateFrequency(queries[i]);
            results[i] = freq[f];
        }
        
        return results;
    }
    public int calculateFrequency(String str)
    {
        int count = 1;
        char smallestChar = str.charAt(0);
        for(int i = 1; i < str.length() ; i++)
        {
            if(str.charAt(i) < smallestChar)
            {
                smallestChar = str.charAt(i);
                count = 1;
            }
            else if(str.charAt(i) == smallestChar)
            {
                count++;
            }
        }
        return count;
    }




