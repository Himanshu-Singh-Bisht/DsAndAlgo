    public static int maxPower(String s) 
    {
        int currentMax = 1;
        int max = 1;

        for(int i = 0; i < s.length()-1 ; i++) 
        {
            if(s.charAt(i) == s.charAt(i+1)) 
            {
                max++;
            }
            else 
            {
                if (max > currentMax) 
                {
                    currentMax = max;
                }
                max = 1;
            }
        }
        return Math.max(currentMax, max);
    }



