    // LeetCode - 819 ___________________________________________
    public static String mostCommonWord(String paragraph, String[] banned) 
    {
        HashSet<String> banned_words = new HashSet<String>();
        HashMap<String , Integer> valid_word_count = new HashMap<>();
        
        for(String str : banned)
        {
            banned_words.add(str);
        }
        
        String[] words = paragraph.toLowerCase().split("\\W+");   // \W means non word character
        
        for(String word : words)
        {
            if(!banned_words.contains(word))
            {
                valid_word_count.put(word , valid_word_count.getOrDefault(word , 0) + 1);
            }
        }
        
        int max = 0;
        String ans = "";
        
        for(String word : valid_word_count.keySet())
        {
            if(valid_word_count.get(word) > max)
            {
                max = valid_word_count.get(word);
                ans = word;
            }
        }
        return ans;
    }







