    public static boolean isPathCrossing(String path)
    {
        int x = 0 , y = 0;
        HashSet<String> set = new HashSet<>();
        set.add("(0,0)");
        for(char ch : path.toCharArray())
        {
            switch(ch){
                case 'N':y++;break;
                case 'S':y--;break;
                case 'E':x++;break;
                case 'W':x--;break;
            }
            
            String str = "(" + x + "," + y + ")";
            if(set.contains(str))
            {
                return true;
            }
            else
            {
                set.add(str);
            }
        }
        return false;
    }




