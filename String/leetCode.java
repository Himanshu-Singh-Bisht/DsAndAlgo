    public static int balancedStringSplit(String str)
    {
        int l = 0 , r = 0 , ans = 0;
        for(int i = 0 ; i < str.length() ; i++)
        {
            if(str.charAt(i) == 'L')
            {
                l++;
            }
            else
            {
                r++;
            }
            if(l == r)
            {
                ans++;
                l = 0;
                r = 0;
            }
        }
        return ans;
    }



